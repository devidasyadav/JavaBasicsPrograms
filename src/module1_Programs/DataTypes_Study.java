package module1_Programs;

public class DataTypes_Study 
{

	public static void main(String[] args) 
	{
		/*Data type study there are two ways by which we can initialize and declare variable
		 * 1. declaration and then initialization
		 * 2.  declaration & initialization in same line*/
		byte a;//type 1 , declaration 
		a=10; // initialization 
		System.out.println(a);//Utilization
		
		short b=654; // type 2,  declaration & initialization in same line 
		
		int c=90000;
		
		long d=9552354125L;
		float d1=85.21f;
		double d2=8.46454646464;
		char c1='a';
		boolean flag=true;
		String cls1="Data type study is easy"; //
		//printing variables
		
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(d1);
		System.out.println(d2);
		System.out.println(c1);
		System.out.println(flag);
		System.out.println(cls1);
		
	}

}
